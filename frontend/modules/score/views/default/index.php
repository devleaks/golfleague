<h1>The Scorer</h1>
<p>
    The role of the scorer is to organise the competitions after it happened.
</p>
<p>
    The main tasks of the Scorer are:
</p>

During the competition or after:
<ul>
	<li>Enter player scores.</li>
</ul>

After the competition ends, for the competition, the Scorer:
<ul>
	<li>Enter, verify and approve scores,</li>
	<li>Compute player positions based on scores,</li>
	<li>Compute player points based on positions,</li>
	<li>Close the competition.</li>
</ul>

After the competition is closed, and if it is part of a Tournament or Season:
<ul>
	<li>Update tournament and/or season points,</li>
	<li>Close the tournament and/or season if the competition was the last one.</li>
</ul>

